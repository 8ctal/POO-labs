    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        Assistants ass = null;
        switch (rbGroupMaritalSt.getSelection().getActionCommand()) {
            case "MARRIED" ->
                ass = new Assistants(Integer.parseInt(txtAge.getText()), chbMale.isSelected(),
                        MaritalStatus.MARRIED);
            case "SINGLE" ->
                ass = new Assistants(Integer.parseInt(txtAge.getText()), chbMale.isSelected(),
                        MaritalStatus.SINGLE);
            case "WIDOW" ->
                ass = new Assistants(Integer.parseInt(txtAge.getText()), chbMale.isSelected(),
                        MaritalStatus.WIDOW);
            case "DIVORCED" ->
                ass = new Assistants(Integer.parseInt(txtAge.getText()), chbMale.isSelected(),
                        MaritalStatus.DIVORCED);
            default ->
                throw new AssertionError();
        }
        assistants.add(ass);
        modResume.clear();
        modResume.addElement("Total Assitants: " + assistants.size());
        modResume.addElement("Total Adults: " + ass.totalAge(assistants)[0]);
        modResume.addElement("Total Minors: " + ass.totalAge(assistants)[1]);
        modResume.addElement("Total Males: " + ass.totalSex(assistants).get(Sex.MALE));
        modResume.addElement("Total Females: " + ass.totalSex(assistants).get(Sex.FEMALE));
        modResume.addElement("Total Single: " + ass.totalMrStatus(assistants).get(MaritalStatus.SINGLE));
        modResume.addElement("Total Married: " + ass.totalMrStatus(assistants).get(MaritalStatus.MARRIED));
        modResume.addElement("Total Widow: " + ass.totalMrStatus(assistants).get(MaritalStatus.WIDOW));
        modResume.addElement("Total Divorced: " + ass.totalMrStatus(assistants).get(MaritalStatus.DIVORCED));
        txtAge.setText("");
        txtAge.requestFocus();

    }//GEN-LAST:event_btnProcessActionPerformed

