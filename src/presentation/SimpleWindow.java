    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Product product = new Product(txtName.getText(), String.valueOf(cboType.getSelectedItem()), Float.parseFloat(txtPrice.getText()), Integer.parseInt(txtAmount.getText()));
        WriterReader stock = new WriterReader();
        try {
            stock.writeProducts(product);
            JOptionPane.showMessageDialog(null, "Product sucessfully added", "", 1);
        } catch (IOException ex) {
            Logger.getLogger(SimpleWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

